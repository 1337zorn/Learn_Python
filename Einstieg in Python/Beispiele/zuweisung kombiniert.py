# Kombinierte Zuweisungsoperatoren für Zahlen
x = 12
print(x)
x += 3      # erhöhen um
print(x)
x -= 9      # reduzieren um
print(x)
x **= 2     # Quadrieren
print(x)
x *= 3      # Multiplizieren
print(x)
x //= 7     # Ganzzahliges Teilen von x
print(x)
x /= 4      # Teilen von x
print(x)
x %= 2      # Dividieren, Rest berechnen
print(x)

# Kombinierte Zuweisungsoperatoren für Zeichenketten
t = "hallo"
print(t)
t += "python"   # Anhängen an t
print(t)
t *= 3          # Verdreifachen von t
print(t)
